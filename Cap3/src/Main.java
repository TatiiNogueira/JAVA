public class Main {

    // Método principal da aplicação
    public void main(){

        // Declaração de uma variável do tipo String para guardar o nome
        String nome = "Tati";

        // Declaração de uma variável inteira para guardar a idade
        int idade = 255;

        // Imprime o nome na consola
        System.out.println(nome + "\n" + idade);


        int a = 10; // Variável inteira com valor 10
        int b = 13; // Variável inteira com valor 13

        // Calcula e imprime a soma de a e b
        System.out.println("Soma: " + (a + b));

        // Calcula e imprime a média de a e b
        // Nota: como são inteiros, o resultado será um número inteiro (sem casas decimais)
        System.out.println("Média: " + (a + b) / 2);
    }
}